// Helper functions
export const getRandomItem = <T>(arr: T[]): T => arr[Math.floor(Math.random() * arr.length)];

export const getRandomNumber = (min: number, max: number): number => 
  Math.floor(Math.random() * (max - min + 1)) + min;

export const getRandomDecimal = (min: number, max: number, decimals: number = 2): number => 
  parseFloat((Math.random() * (max - min) + min).toFixed(decimals));

export const getWeightedBloodType = (): string => {
  const rand = Math.random() * 100;
  let cumulative = 0;
  for (const [type, percentage] of Object.entries(medicalResearchData.bloodTypeDistribution)) {
    cumulative += percentage;
    if (rand < cumulative) return type;
  }
  return 'O+';
};
